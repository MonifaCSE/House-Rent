       jRentreceipt.append("Address"+"\t\t\t"+jAddress.getText()+"\n");
       jRentreceipt.append("Post Code"+"\t\t\t"+jPostcode.getText()+"\n");
       jRentreceipt.append("Town"+"\t\t\t"+jTown.getText()+"\n");
       jRentreceipt.append("Location"+"\t\t\t"+jLocation.getSelectedItem()+"\n");
       jRentreceipt.append("Number of room"+"\t\t"+jNumberofroom.getSelectedItem()+"\n");
