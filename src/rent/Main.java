         Rental iRental = new Rental();
         UtilityBill iUtilityBill = new UtilityBill();
         
         double iCustDeposit,iCustPayment,iCost,iNumberofroom,iLocation,iLocaltax,iWaterbill,iElectricitybill;
         String strCost;
      iCustDeposit = Double.parseDouble(jDeposit.getText());
      iCustPayment = Double.parseDouble(jDownpayment.getText());
